/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  //lcd_write(I2C1,2);


  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();

  rgb_lcd lcd_data;
  lcd_init(&hi2c1, &lcd_data); // Initialisation du LCD
  clearlcd(); // Effacer l'écran

  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  uint8_t commande[2] = {0x21, 0x26};
  uint8_t data[6];
  uint16_t temp;
  uint16_t humid;
  float temperature;
  float humidite;

  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  HAL_I2C_Master_Transmit(&hi2c1, (0x44 << 1), commande, 2, 5000);
	  HAL_Delay(50);
	  HAL_I2C_Master_Receive(&hi2c1, (0x44 << 1), data, 6, 5000) ;
	  HAL_Delay(50);

	  // Température
	  temp = (data[0] << 8) | data[1] ;
	  temperature = -45 + 175*(temp/(65535 + 0.0));

	  // Humidité
	  humid = (data[3] << 8) | data [4];
	  humidite = 100*(humid/(65535 + 0.0));

	  // Affichage LCD
	  char str[20]; // Déclaration d'un buffer pour stocker les chaînes de caractères à afficher

	  sprintf(str, "Temp. : %.2f C", temperature); // Formater la température avec une seule décimale et l'ajouter dans la chaîne 'str'
	  lcd_position(&hi2c1, 0, 0); // Positionner le curseur du LCD à la ligne 0, colonne 0
	  lcd_print(&hi2c1, str); // Afficher la température sur l'écran LCD

	  sprintf(str, "Hum. : %.2f %%", humidite); // Formater l'humidité avec une seule décimale et l'ajouter dans la chaîne 'str'
	  lcd_position(&hi2c1, 0, 1); // Positionner le curseur du LCD à la ligne 1, colonne 0
	  lcd_print(&hi2c1, str); // Afficher l'humidité sur l'écran LCD

	  HAL_Delay(2000);

  /* USER CODE END 3 */
  }
}
